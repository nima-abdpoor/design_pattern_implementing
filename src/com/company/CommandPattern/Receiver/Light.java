package com.company.CommandPattern.Receiver;

public class Light{
    public void On(){
        System.out.println("The Light Is On!");
    }
    public void Off(){
        System.out.println("The Light Is Off!");
    }
    public void Up(){
        System.out.println("The Light Is Going Up...");
    }
    public void Down(){
        System.out.println("The Light Is Going Down...");
    }
}
